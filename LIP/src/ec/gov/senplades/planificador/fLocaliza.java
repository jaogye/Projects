     private void CargaArbol() {
	     
	   
       DefaultMutableTreeNode Ecuador = new DefaultMutableTreeNode( "Ecuador" );
       modelo = new DefaultTreeModel(Ecuador);
       jTreeTerritorio.setModel(modelo);
       
       jTreeTerritorio.setEditable(true);
       DefaultMutableTreeNode Azuay = new DefaultMutableTreeNode( "Azuay" );
       DefaultMutableTreeNode Bolivar = new DefaultMutableTreeNode( "Bolivar" );
       DefaultMutableTreeNode Cañar = new DefaultMutableTreeNode( "Cañar" );       
       
       modelo.insertNodeInto(Azuay, Ecuador,0);
       modelo.insertNodeInto(Bolivar,Ecuador, 1);       
       modelo.insertNodeInto(Cañar, Ecuador,2);
       
       DefaultMutableTreeNode Cuenca = new DefaultMutableTreeNode("Cuenca");
       DefaultMutableTreeNode Giron = new DefaultMutableTreeNode("Giron");
       DefaultMutableTreeNode Gualaceo=new DefaultMutableTreeNode("Gualaceo");
       DefaultMutableTreeNode Guaranda = new DefaultMutableTreeNode("Guaranda");
       DefaultMutableTreeNode Chillanes=new DefaultMutableTreeNode("Chillanes");
       DefaultMutableTreeNode Chimbo=new DefaultMutableTreeNode("Chimbo");
       DefaultMutableTreeNode Azogues = new DefaultMutableTreeNode("Azoguez");
       DefaultMutableTreeNode Biblian=new DefaultMutableTreeNode("Biblian");
       DefaultMutableTreeNode Cañar2=new DefaultMutableTreeNode("Cañar");

       modelo.insertNodeInto(Cuenca, Azuay, 0);
       modelo.insertNodeInto(Giron, Azuay, 1);
       modelo.insertNodeInto(Gualaceo, Azuay,2);
       modelo.insertNodeInto(Guaranda,  Bolivar,0);
       modelo.insertNodeInto(Chillanes,  Bolivar,1);
       modelo.insertNodeInto(Chimbo, Bolivar, 2);       
       modelo.insertNodeInto(Azogues, Cañar,0);       
       modelo.insertNodeInto(Biblian, Cañar,1);       
       modelo.insertNodeInto(Cañar2, Cañar,2);
       
       DefaultMutableTreeNode Cuenca2=new DefaultMutableTreeNode("Cuenca");
       DefaultMutableTreeNode Bellavista=new DefaultMutableTreeNode("Bellavista");       
       DefaultMutableTreeNode Cañaribamba=new DefaultMutableTreeNode("Cañaribamba");       

       modelo.insertNodeInto(Cuenca2 ,Cuenca, 0);
       modelo.insertNodeInto(Bellavista ,Cuenca, 1);
       modelo.insertNodeInto(Cañaribamba ,Cuenca, 2);
       
       DefaultMutableTreeNode Giron2=new DefaultMutableTreeNode("Giron");       
       DefaultMutableTreeNode Asuncion2=new DefaultMutableTreeNode("Asuncion");

       modelo.insertNodeInto(Giron2 ,Giron,0);
       modelo.insertNodeInto(Asuncion2 ,Giron,1);
       
       DefaultMutableTreeNode Gualaceo2=new DefaultMutableTreeNode("Gualaceo");       
       DefaultMutableTreeNode Daniel=new DefaultMutableTreeNode("Daniel Cordova Toral");

       modelo.insertNodeInto(Gualaceo2 ,Gualaceo,0);       
       modelo.insertNodeInto(Daniel ,Gualaceo,1);

       DefaultMutableTreeNode Guaranda2=new DefaultMutableTreeNode("Guaranda");       
       DefaultMutableTreeNode Angel=new DefaultMutableTreeNode("Angel Polivio Chavez");
       
       modelo.insertNodeInto(Guaranda2 ,Guaranda,0);
       modelo.insertNodeInto(Angel ,Guaranda,1);              
       
       DefaultMutableTreeNode Chillanes2=new DefaultMutableTreeNode("Chillanes");       
       DefaultMutableTreeNode Tambo=new DefaultMutableTreeNode("San Jose del Tambo");

       modelo.insertNodeInto(Chillanes2 ,Chillanes, 0);
       modelo.insertNodeInto(Tambo ,Chillanes, 1);
       
       DefaultMutableTreeNode Chimbo2=new DefaultMutableTreeNode("Chimbo");       
       DefaultMutableTreeNode LaAsuncion=new DefaultMutableTreeNode("La Asuncion");

       modelo.insertNodeInto(Chimbo2 ,Chimbo, 0);
       modelo.insertNodeInto(LaAsuncion ,Chimbo, 1);       
       
       DefaultMutableTreeNode Azogues2=new DefaultMutableTreeNode("Azogues");       
       DefaultMutableTreeNode Borrero=new DefaultMutableTreeNode("Borrero");

       modelo.insertNodeInto(Azogues2 ,Azogues, 0);       
       modelo.insertNodeInto(Borrero ,Azogues, 1);
       
       DefaultMutableTreeNode Biblian2=new DefaultMutableTreeNode("Biblian");       
       DefaultMutableTreeNode Nazon=new DefaultMutableTreeNode("Nazon");

       modelo.insertNodeInto( Biblian2 ,Biblian,0);       
       modelo.insertNodeInto( Nazon ,Biblian,1);       
       
       DefaultMutableTreeNode Cañar3=new DefaultMutableTreeNode("Cañar");       
       DefaultMutableTreeNode ChontaMarca=new DefaultMutableTreeNode("Chontamarca");

       modelo.insertNodeInto( Cañar3 ,Cañar2,0);
       modelo.insertNodeInto( ChontaMarca ,Cañar2,1);       
       
       jTreeTerritorio.setModel(modelo);
   }


